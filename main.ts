// switch statements examples


let fruits1=0
switch(fruits1){
    case 0 :
        console.log("i am apple");
        break;
    case 1:
        console.log("i am banana");
        break;
    case 2:
        console.log("i am lychee");
        break;
    case 3:
        console.log("i am mango");
        break;
default:{
        console.log("invalid fruit");
        break;
    }
}



